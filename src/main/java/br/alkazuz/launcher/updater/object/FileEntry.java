package br.alkazuz.launcher.updater.object;

public class FileEntry {
    private String file;
    private String hash;

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getHash() {
        return hash;
    }

    public void setHash(String hash) {
        this.hash = hash;
    }
}

